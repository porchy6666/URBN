const modal = document.getElementById("modal");
const gallery = document.getElementById("modalGallery");

// клик по карточке
document.querySelectorAll(".masters__card").forEach(card => {
  card.addEventListener("click", () => {
    const id = card.dataset.master;
    const master = masters.find(m => m.id === id);
    if (!master) return; // защита
    gallery.innerHTML = "";
    gallery.classList.remove("single");
    master.works.forEach(src => {
      const img = document.createElement("img");
      img.src = src;
      gallery.appendChild(img);
    });

    // открываю модалку
    modal.classList.add("active");
    document.body.style.overflow = "hidden"
  });
});

modal.addEventListener("click", (e) => {
  if (e.target === modal) {
    modal.classList.remove("active");
document.body.style.overflow = "";
  }
});


gallery.addEventListener("click", (e) => {
  if (e.target.tagName === "IMG") {
    const src = e.target.src;

    gallery.innerHTML = `
      <img src="${src}" class="modal__big-img">
    `;
    gallery.classList.add("single");
  }
});


document.querySelectorAll(".galary__img").forEach(img => {
  img.addEventListener("click", () => {
    gallery.innerHTML = "";

    const bigImg = document.createElement("img");
    bigImg.src = img.src;
    bigImg.classList.add("modal__big-img");

    gallery.appendChild(bigImg);

    modal.classList.add("active");
    document.body.style.overflow = "hidden";
  });
});


